/** slug 정규화 (URL 디코딩·trim) */
function normalizeSlug(slug: string): string {
  try {
    return decodeURIComponent(slug).trim();
  } catch {
    return slug.trim();
  }
}

/** slug로 글 조회 (인코딩된 URL 대응) */
export function getGuidePost(slug: string): GuidePost | undefined {
  if (!slug) return undefined;
  const normalized = normalizeSlug(slug);
  return GUIDE_POSTS.find((p) => p.slug === normalized) ?? GUIDE_POSTS.find((p) => p.slug === slug);
}

/** 모든 slug 목록 (정적 생성·사이트맵용) */
export function getAllGuideSlugs(): string[] {
  return GUIDE_POSTS.map((p) => p.slug);
}

/** 오피가이드 배너제작을 최상위로 한 정렬된 가이드 목록 (가이드 페이지·노출 순서용) */
const OPIGUIDE_SLUG = "오피가이드-배너-제작";
function sortGuideWithOpiguideFirst(posts: GuidePost[]): GuidePost[] {
  return [...posts].sort((a, b) =>
    a.slug === OPIGUIDE_SLUG ? -1 : b.slug === OPIGUIDE_SLUG ? 1 : 0
  );
}

/** 카테고리별 가이드 목록 (키워드 허브 페이지 — 플랫폼 목록에 오피가이드 배너제작 1번) */
export function getGuidePostsByCategory(): Record<GuideCategory, GuidePost[]> {
  const platform = sortGuideWithOpiguideFirst(
    GUIDE_POSTS.filter((p) => p.category === "platform")
  );
  const region = GUIDE_POSTS.filter((p) => p.category === "region");
  return { platform, region };
}

/** 정렬된 전체 가이드 목록 (가이드 페이지 목록용, 오피가이드 1번) */
export function getOrderedGuidePosts(): GuidePost[] {
  return sortGuideWithOpiguideFirst(GUIDE_POSTS);
}
